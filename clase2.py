
nombre = "Jose Enrique"
valor1 = 12
valor2 = 3.1416

print("El nombre es ", nombre)
print("el valor entero es ", valor1)
print("El valor real es ", valor2)
# En Python, la división de 2 enteros da un real
r = valor1 / 5
print("El resultado de la división real es ", r)
# En Python, la división entera se representa mediante //
r2 = valor1 // 5
print("El resultado de la división entera es ", r2)
# En Python, el residuo o módulo se representa mediante %
r3 = valor1 % 5
print("El residuo de la división entera es ", r3)
